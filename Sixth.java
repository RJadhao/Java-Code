public class Sixth {
    public static void main(String[] args) {
        short a=10;
        byte b=(byte)a;
        System.out.println("a = "+a);
        System.out.println("b = "+b);
        System.out.println();
        double c=10.0;
        float d=(float)c;
        System.out.println("c = "+c);
        System.out.println("d = "+d);
    }
}


//byte-->short-->int-->long-->float-->double